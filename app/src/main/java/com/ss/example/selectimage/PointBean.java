package com.ss.example.selectimage;

public class PointBean {

    /**
     * startX : 80
     * endX : 270
     * startY : 1000
     * endY : 1166
     */

    private int startX;
    private int endX;
    private int startY;
    private int endY;

    public int getStartX() {
        return startX;
    }

    public void setStartX(int startX) {
        this.startX = startX;
    }

    public int getEndX() {
        return endX;
    }

    public void setEndX(int endX) {
        this.endX = endX;
    }

    public int getStartY() {
        return startY;
    }

    public void setStartY(int startY) {
        this.startY = startY;
    }

    public int getEndY() {
        return endY;
    }

    public void setEndY(int endY) {
        this.endY = endY;
    }
}
